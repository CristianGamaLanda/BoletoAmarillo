    public void Banorte() {
        //CALCULO DE IMPORTE
        precio = 0;
        if (nhora > 0 && nhora < 3) {
            precio = 6;
        }
        if (nhora > 2) {
            precio = ((nhora - 2) * 6 + 6 * (nhora - 3));
            if (nminutos > 0 && nminutos <= 15) {
                precio += 3;
            }
            if (nminutos > 15 && nminutos <= 30) {
                precio += 6;
            }
            if (nminutos > 30 && nminutos <= 45) {
                precio += 9;
            }
            if (nminutos > 45 && nminutos <= 60) {
                precio += 12;
            }
        }
        //System.out.println("PRECIO A PAGAR: $" + precio);
        tImporte.setText("" + precio);
    }

